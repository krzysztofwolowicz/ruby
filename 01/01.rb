W naszym kursie zapoznamy się, z:
- instalacją Ruby w systemie windows,
- podstawowymi komendami,
- metodami,
- przedstawimy czym są obiekty i dlaczego nowoczesne języki programowania są właśnie obiektowe,
- na koniec wykonamy kilka praktycznych przykładów,
- jako dodatek przedstawię najpopularniejsze edytory ułatwiające pracę z Ruby.
=end