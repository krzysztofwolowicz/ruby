=begin
Komendy wpisywane w wierszu poleceń:

1. sprawdzenie wersji Ruby:
>ruby -v  

2. Wyświetlenie tekstu "witaj!":
>ruby -e 'print "witaj!" '
>ruby -e "print \"witaj!\" "

3. Uruchomienie Ruby:
>irb

4. Uruchomienie Ruby - tryb tylko znak ">":
>irb --simple-prompt

5. Wyjście z Ruby:
>quit

6. Wyczyszczenie okna w Ruby:
>system 'cls'

