# Boolean

# przypisanie wartosci do zmienne x:
x = 1

# porownanie zmiennej x do wartosci (sprawdza czy x jest rowne 1?):
x == 1
# czy  x jest rowne 2?
x == 2

# czy x jest rozne od 1?
x != 1
# czy x jest rozne od 2?
x != 2

# czy x jest mniejsze 2?
x < 2

# czy x jest wieksze 3?
x > 3

# czy x jest wieksze lub rowne 0?
x >= 0

# czy x jest mniejsze lub rowne 2?
x <= 2

# przyklady:
x = 5
x >= 1
x = 10

# czy x jest wieksze rowne 1 i mniejsze rowne 10 (musza byc spelnione oba wyrazenia)?
x >= 1 and x <= 10

# jak wyzej - lukier skladniowy:
x >= 1 && x <= 10

# czy x jest wieksze rowne 1 lub wieksze rowne 100 (musi byc spelniony jedno wyrazenie)?
x >= 1 or x >= 100

# jak wyzej - lukier skladniowy:
x >= 1 || x >= 100

# rozbudowany przyklad:
x >= 1 || x >= 100 || x == 90 || x == 2
